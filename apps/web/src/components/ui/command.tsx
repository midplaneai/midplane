import * as React from "react";

import { cn } from "@/lib/utils";

// cmdk wrapper themed to the design system. Hosts the searchable option list
// inside a faceted-filter Popover. Rectangular surfaces, mono type, semantic
// hover/selection via `--accent`.

const Command = React.forwardRef<
  React.ElementRef<typeof CommandPrimitive>,
  React.ComponentPropsWithoutRef<typeof CommandPrimitive>
>(({ className, ...props }, ref) => (
  <CommandPrimitive
    ref={ref}
    className={cn("flex w-full flex-col overflow-hidden bg-popover", className)}
    {...props}
  />
));
Command.displayName = CommandPrimitive.displayName;

